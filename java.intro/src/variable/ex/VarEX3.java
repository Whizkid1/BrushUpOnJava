package variable.ex;

public class VarEX3 {
    public static void main(String[] args) {
        // long type 변수에 백억 초기화
        long longNum = 10_000_000_000L;
        // 출력
        System.out.println(longNum);
        // boolean 타입의 변수를 선언
        boolean booleanVar;
        // true로 초기화
        booleanVar = true;
        // 출력
        System.out.println(booleanVar);
    }
}
